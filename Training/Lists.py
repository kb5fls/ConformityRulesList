
# Lists are used to store data of different data types in a sequential manner
list = [1, 2, 3, 10, 30, 10, 10, 20, 30, 40, 60, 50, 70, 90, 80, 100]
print(len(list)) # Prints length of list
print(list.index(10)) # prints index of the first 10 in the list
print(list.count(30)) # print number of occurrences of the number 30
print(sorted(list)) # prints list in numerical order
list.sort(reverse=True) # prints list in reverse sorted order
print(list)
